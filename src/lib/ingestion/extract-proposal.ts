/**
 * Per-line item shape AFTER dollars→cents normalization. The `*_cents`
 * fields are integers (or null when the proposal didn't break out the
 * category — never inferred). `attributes` carries free-form structured
 * specs (species, grade, dimensions) when the line is material-based.
 */
export type ParsedProposalLineItem = {
  line_number: number;
  description: string;
  description_normalized: string | null;
  quantity: number | null;
  unit_of_measure: string | null;
  unit_price_cents: number | null;
  total_price_cents: number;
  cost_code_suggestion: string | null;
  material_cost_cents: number | null;
  labor_cost_cents: number | null;
  subcontract_cost_cents: number | null;
  tax_cents: number | null;
  delivery_cents: number | null;
  notes_cents: number | null;
  attributes: Record<string, unknown>;
};

export type ParsedProposal = {
  // Header
  vendor_name: string;
  vendor_address: string | null;
  proposal_number: string | null;
  proposal_date: string | null; // YYYY-MM-DD
  valid_through: string | null; // YYYY-MM-DD

  // Generated by AI from header content
  title: string;

  // Total
  total_cents: number;

  // Scope
  scope_summary: string;
  inclusions: string | null;
  exclusions: string | null;
  notes: string | null;

  // Vendor schedule — explicit only, never inferred
  vendor_stated_start_date: string | null;
  vendor_stated_duration_days: number | null;

  // Lines
  line_items: ParsedProposalLineItem[];

  // Confidence
  confidence_score: number; // [0, 1]
  confidence_details: Record<string, number>;

  // Concerns flagged by the model (e.g. 'lump_sum', 'math_mismatch')
  flags: string[];

  // Pre-normalization Claude payload — written to proposals.raw_extraction
  // JSONB so the audit trail keeps the dollars-shape exactly as returned.
  raw_response: unknown;
};

