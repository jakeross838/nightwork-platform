export type ExtractProposalMeta = {
  org_id: string;
  user_id?: string | null;
  metadata?: Record<string, unknown>;
};

/**
 * Per-line item shape AFTER dollars→cents normalization. The `*_cents`
 * fields are integers (or null when the proposal didn't break out the
 * category — never inferred). `attributes` carries free-form structured
 * specs (species, grade, dimensions) when the line is material-based.
 */
export type ParsedProposalLineItem = {
  line_number: number;
  description: string;
  description_normalized: string | null;
  quantity: number | null;
  unit_of_measure: string | null;
  unit_price_cents: number | null;
  total_price_cents: number;
  cost_code_suggestion: string | null;
  material_cost_cents: number | null;
  labor_cost_cents: number | null;
  subcontract_cost_cents: number | null;
  tax_cents: number | null;
  delivery_cents: number | null;
  notes_cents: number | null;
  attributes: Record<string, unknown>;
};

/**
 * Fee schedule entry — one row from a proposal's separate hourly /
 * blended rate table (common on professional-services proposals where
 * the base scope is a fixed fee but additional time is billed per
 * hour). Phase 3.4 Step 5b/5c.
 */
export type ParsedProposalFeeScheduleEntry = {
  rate_type: string; // short slug, e.g. "hourly", "principal_hour"
  description: string | null;
  rate_cents: number | null;
  unit: string | null; // "hr", "day", "visit", etc.
};

/**
 * Payment schedule entry — one billing milestone with either a
 * percentage of total, a dollar amount, or both. Phase 3.4 Step 5b/5c.
 */
export type ParsedProposalPaymentScheduleEntry = {
  milestone: string; // short slug, e.g. "deposit", "delivery"
  percentage_pct: number | null;
  amount_cents: number | null;
  trigger: string | null; // when the payment is owed
};

/**
 * Payment terms object — top-level vendor billing terms. Each sub-field
 * nullable; populate only fields explicitly stated on the proposal.
 * Phase 3.4 Step 5b/5c.
 */
export type ParsedProposalPaymentTerms = {
  net_days: number | null;
  late_interest_rate_pct: number | null;
  governing_law: string | null;
  other_terms_text: string | null;
};

/**
 * Schedule item — one structured chunk of a phased / sequenced proposal
 * scope. Phase 4 schedule-intelligence foundation per amendment-1.
 * Captured at proposal extract time (Step 5g); surfaced when Phase 3.5
 * ships the PO detail tab. Phase 3.4 Step 5f/5g.
 */
export type ParsedProposalScheduleItem = {
  scope_item: string;
  linked_line_number: number | null;
  estimated_start_date: string | null; // YYYY-MM-DD
  estimated_duration_days: number | null;
  sequence_position: number | null;
  depends_on: number[];
  responsibility: string | null; // "vendor" | "contractor" | "shared"
  deliverables: string[];
  trigger: string | null;
};

export type ParsedProposal = {
  // Header
  vendor_name: string;
  vendor_address: string | null;
  proposal_number: string | null;
  proposal_date: string | null; // YYYY-MM-DD
  valid_through: string | null; // YYYY-MM-DD

  // Generated by AI from header content
  title: string;

  // Total
  total_cents: number;

  // Scope
  scope_summary: string;
  inclusions: string | null;
  exclusions: string | null;
  notes: string | null;

  // Vendor schedule — explicit only, never inferred
  vendor_stated_start_date: string | null;
  vendor_stated_duration_days: number | null;

  // Lines
  line_items: ParsedProposalLineItem[];

  // Fee + payment structures (Phase 3.4 Step 5b/5c). Each is null when
  // the proposal doesn't include the structure; never inferred. Mirror
  // the migration 00088 column shape so commit can persist directly.
  additional_fee_schedule: ParsedProposalFeeScheduleEntry[] | null;
  payment_schedule: ParsedProposalPaymentScheduleEntry[] | null;
  payment_terms: ParsedProposalPaymentTerms | null;

  // Schedule intelligence + acceptance signature (Phase 3.4 Step 5f/5g).
  // schedule_items: null when no schedulable structure on proposal.
  // accepted_signature_*: name/date null when block blank or absent;
  // present is the load-bearing boolean (NOT NULL on the column).
  schedule_items: ParsedProposalScheduleItem[] | null;
  accepted_signature_present: boolean;
  accepted_signature_name: string | null;
  accepted_signature_date: string | null;

  // Project/install/site address (Phase 3.4 Step 5j/5k). Null when
  // proposal doesn't state a site address.
  job_address: string | null;

  // Confidence
  confidence_score: number; // [0, 1]
  confidence_details: Record<string, number>;

  // Concerns flagged by the model (e.g. 'lump_sum', 'math_mismatch')
  flags: string[];

  // Pre-normalization Claude payload — written to proposals.raw_extraction
  // JSONB so the audit trail keeps the dollars-shape exactly as returned.
  raw_response: unknown;
};

// ─────────────────────────────────────────────────────────────────
// System prompt — exported for prompt-iteration eval (Step 6B)
