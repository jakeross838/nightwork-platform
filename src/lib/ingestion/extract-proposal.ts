/**
 * Normalize an additional_fee_schedule array. Returns null when the AI
 * emitted null (proposal had no fee table), or filters out invalid
 * entries from any array. Entries with a non-string rate_type are
 * dropped — that's the load-bearing identifier.
 */
function normalizeFeeSchedule(
  v: unknown
): ParsedProposalFeeScheduleEntry[] | null {
  if (v === null || v === undefined) return null;
  if (!Array.isArray(v)) return null;
  const out: ParsedProposalFeeScheduleEntry[] = [];
  for (const e of v as RawFeeScheduleEntry[]) {
    if (!e || typeof e !== "object") continue;
    if (typeof e.rate_type !== "string" || e.rate_type.length === 0) continue;
    out.push({
      rate_type: e.rate_type,
      description: asStringOrNull(e.description),
      rate_cents: dollarsToCents(e.rate),
      unit: asStringOrNull(e.unit),
    });
  }
  // If the array existed but every entry was invalid, return null
  // rather than [] so downstream code can render "no fee schedule"
  // consistently with the no-array case.
  return out.length > 0 ? out : null;
}

/**
 * Normalize a payment_schedule array. Same null/empty behavior as
 * normalizeFeeSchedule. Entries with a non-string milestone are dropped.
 */
function normalizePaymentSchedule(
  v: unknown
): ParsedProposalPaymentScheduleEntry[] | null {
  if (v === null || v === undefined) return null;
  if (!Array.isArray(v)) return null;
  const out: ParsedProposalPaymentScheduleEntry[] = [];
  for (const e of v as RawPaymentScheduleEntry[]) {
    if (!e || typeof e !== "object") continue;
    if (typeof e.milestone !== "string" || e.milestone.length === 0) continue;
    out.push({
      milestone: e.milestone,
      percentage_pct: asNumberOrNull(e.percentage_pct),
      amount_cents: dollarsToCents(e.amount),
      trigger: asStringOrNull(e.trigger),
    });
  }
  return out.length > 0 ? out : null;
}

/**
 * Normalize a schedule_items array. Returns null when the AI emitted
 * null OR every entry was invalid. Entries with a non-string scope_item
 * are dropped. depends_on is filtered to integers only; deliverables
 * to strings only.
 */
function normalizeScheduleItems(
  v: unknown
): ParsedProposalScheduleItem[] | null {
  if (v === null || v === undefined) return null;
  if (!Array.isArray(v)) return null;
  const out: ParsedProposalScheduleItem[] = [];
  for (const e of v as RawScheduleItem[]) {
    if (!e || typeof e !== "object") continue;
    if (typeof e.scope_item !== "string" || e.scope_item.length === 0) continue;
    const depends_on = Array.isArray(e.depends_on)
      ? (e.depends_on as unknown[])
          .map((n) => (typeof n === "number" && Number.isFinite(n) ? Math.round(n) : null))
          .filter((n): n is number => n !== null)
      : [];
    const deliverables = Array.isArray(e.deliverables)
      ? (e.deliverables as unknown[]).filter(
          (s): s is string => typeof s === "string" && s.length > 0
        )
      : [];
    out.push({
      scope_item: e.scope_item,
      linked_line_number: asIntOrNull(e.linked_line_number),
      estimated_start_date: asStringOrNull(e.estimated_start_date),
      estimated_duration_days: asIntOrNull(e.estimated_duration_days),
      sequence_position: asIntOrNull(e.sequence_position),
      depends_on,
      responsibility: asStringOrNull(e.responsibility),
      deliverables,
      trigger: asStringOrNull(e.trigger),
    });
  }
  return out.length > 0 ? out : null;
}

/**
 * Normalize the payment_terms object. Returns null when the AI emitted
 * null OR when every sub-field is null after coercion (a defensively
 * empty object is treated the same as no terms — matches the prompt
 * rule "don't return an object with all-null sub-fields").
 */
function normalizePaymentTerms(
  v: unknown
): ParsedProposalPaymentTerms | null {
  if (v === null || v === undefined) return null;
  if (typeof v !== "object" || Array.isArray(v)) return null;
  const r = v as RawPaymentTerms;
  const net_days = asIntOrNull(r.net_days);
  const late_interest_rate_pct = asNumberOrNull(r.late_interest_rate_pct);
  const governing_law = asStringOrNull(r.governing_law);
  const other_terms_text = asStringOrNull(r.other_terms_text);
  if (
    net_days === null &&
    late_interest_rate_pct === null &&
    governing_law === null &&
    other_terms_text === null
  ) {
    return null;
  }
  return { net_days, late_interest_rate_pct, governing_law, other_terms_text };
}

/**
 * Convert the AI's dollars-shape payload into the cents-shape ParsedProposal.
 * Throws on missing required fields (vendor_name, title, total_amount, per-line
 * description + amount) — these are not recoverable; the prompt is at fault.
 *
 * Exported so the eval (Step 6B) can re-run normalization against captured
 * raw responses without re-paying for AI calls.
 */
export function normalizeProposal(raw: RawProposal): ParsedProposal {
  if (typeof raw.vendor_name !== "string" || raw.vendor_name.length === 0) {
    throw new Error("extractProposal: vendor_name is missing or empty");
  }
  if (typeof raw.title !== "string" || raw.title.length === 0) {
    throw new Error("extractProposal: title is missing or empty");
  }
  if (typeof raw.scope_summary !== "string") {
    throw new Error("extractProposal: scope_summary is missing or non-string");
  }

  const totalCents = requireDollarsToCents(raw.total_amount, "total_amount");

  const rawLines = Array.isArray(raw.line_items) ? (raw.line_items as RawLineItem[]) : [];
  const line_items: ParsedProposalLineItem[] = rawLines.map((rl, idx) => {
    if (typeof rl.description !== "string" || rl.description.length === 0) {
      throw new Error(
        `extractProposal: line_items[${idx}].description is missing or empty`
      );
    }
    const lineNumber =
      typeof rl.line_number === "number" && Number.isFinite(rl.line_number)
        ? Math.round(rl.line_number)
        : idx + 1;
    return {
      line_number: lineNumber,
      description: rl.description,
      description_normalized: asStringOrNull(rl.description_normalized),
      quantity: asNumberOrNull(rl.quantity),
      unit_of_measure: asStringOrNull(rl.unit_of_measure),
      unit_price_cents: dollarsToCents(rl.unit_price),
      total_price_cents: requireDollarsToCents(rl.amount, `line_items[${idx}].amount`),
      cost_code_suggestion: asStringOrNull(rl.cost_code_suggestion),
      material_cost_cents: dollarsToCents(rl.material_cost),
      labor_cost_cents: dollarsToCents(rl.labor_cost),
      subcontract_cost_cents: dollarsToCents(rl.subcontract_cost),
      tax_cents: dollarsToCents(rl.tax),
      delivery_cents: dollarsToCents(rl.delivery),
      notes_cents: dollarsToCents(rl.notes_amount),
      attributes: asAttributes(rl.attributes),
    };
  });

  const confidenceDetails =
    raw.confidence_details &&
    typeof raw.confidence_details === "object" &&
    !Array.isArray(raw.confidence_details)
      ? (raw.confidence_details as Record<string, unknown>)
      : {};
  const cleanedConfidenceDetails: Record<string, number> = {};
  for (const [k, v] of Object.entries(confidenceDetails)) {
    if (typeof v === "number" && Number.isFinite(v)) {
      cleanedConfidenceDetails[k] = clamp01(v);
    }
  }

  const flagsArr = Array.isArray(raw.flags) ? raw.flags : [];
  const flags: string[] = flagsArr.filter((s): s is string => typeof s === "string");

  return {
    vendor_name: raw.vendor_name,
    vendor_address: asStringOrNull(raw.vendor_address),
    proposal_number: asStringOrNull(raw.proposal_number),
    proposal_date: asStringOrNull(raw.proposal_date),
    valid_through: asStringOrNull(raw.valid_through),
    title: raw.title,
    total_cents: totalCents,
    scope_summary: raw.scope_summary,
    inclusions: asStringOrNull(raw.inclusions),
    exclusions: asStringOrNull(raw.exclusions),
    notes: asStringOrNull(raw.notes),
    vendor_stated_start_date: asStringOrNull(raw.vendor_stated_start_date),
    vendor_stated_duration_days: asIntOrNull(raw.vendor_stated_duration_days),
    line_items,
    additional_fee_schedule: normalizeFeeSchedule(raw.additional_fee_schedule),
    payment_schedule: normalizePaymentSchedule(raw.payment_schedule),
    payment_terms: normalizePaymentTerms(raw.payment_terms),
    schedule_items: normalizeScheduleItems(raw.schedule_items),
    accepted_signature_present:
      typeof raw.accepted_signature_present === "boolean"
        ? raw.accepted_signature_present
        : false,
    accepted_signature_name: asStringOrNull(raw.accepted_signature_name),
    accepted_signature_date: asStringOrNull(raw.accepted_signature_date),
    job_address: asStringOrNull(raw.job_address),
    confidence_score: clamp01(raw.confidence_score),
    confidence_details: cleanedConfidenceDetails,
    flags,
    raw_response: raw,
  };
}

