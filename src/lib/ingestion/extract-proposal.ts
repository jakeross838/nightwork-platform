/**
 * Convert the AI's dollars-shape payload into the cents-shape ParsedProposal.
 * Throws on missing required fields (vendor_name, title, total_amount, per-line
 * description + amount) — these are not recoverable; the prompt is at fault.
 *
 * Exported so the eval (Step 6B) can re-run normalization against captured
 * raw responses without re-paying for AI calls.
 */
export function normalizeProposal(raw: RawProposal): ParsedProposal {
  if (typeof raw.vendor_name !== "string" || raw.vendor_name.length === 0) {
    throw new Error("extractProposal: vendor_name is missing or empty");
  }
  if (typeof raw.title !== "string" || raw.title.length === 0) {
    throw new Error("extractProposal: title is missing or empty");
  }
  if (typeof raw.scope_summary !== "string") {
    throw new Error("extractProposal: scope_summary is missing or non-string");
  }

  const totalCents = requireDollarsToCents(raw.total_amount, "total_amount");

  const rawLines = Array.isArray(raw.line_items) ? (raw.line_items as RawLineItem[]) : [];
  const line_items: ParsedProposalLineItem[] = rawLines.map((rl, idx) => {
    if (typeof rl.description !== "string" || rl.description.length === 0) {
      throw new Error(
        `extractProposal: line_items[${idx}].description is missing or empty`
      );
    }
    const lineNumber =
      typeof rl.line_number === "number" && Number.isFinite(rl.line_number)
        ? Math.round(rl.line_number)
        : idx + 1;
    return {
      line_number: lineNumber,
      description: rl.description,
      description_normalized: asStringOrNull(rl.description_normalized),
      quantity: asNumberOrNull(rl.quantity),
      unit_of_measure: asStringOrNull(rl.unit_of_measure),
      unit_price_cents: dollarsToCents(rl.unit_price),
      total_price_cents: requireDollarsToCents(rl.amount, `line_items[${idx}].amount`),
      cost_code_suggestion: asStringOrNull(rl.cost_code_suggestion),
      material_cost_cents: dollarsToCents(rl.material_cost),
      labor_cost_cents: dollarsToCents(rl.labor_cost),
      subcontract_cost_cents: dollarsToCents(rl.subcontract_cost),
      tax_cents: dollarsToCents(rl.tax),
      delivery_cents: dollarsToCents(rl.delivery),
      notes_cents: dollarsToCents(rl.notes_amount),
      attributes: asAttributes(rl.attributes),
    };
  });

  const confidenceDetails =
    raw.confidence_details &&
    typeof raw.confidence_details === "object" &&
    !Array.isArray(raw.confidence_details)
      ? (raw.confidence_details as Record<string, unknown>)
      : {};
  const cleanedConfidenceDetails: Record<string, number> = {};
  for (const [k, v] of Object.entries(confidenceDetails)) {
    if (typeof v === "number" && Number.isFinite(v)) {
      cleanedConfidenceDetails[k] = clamp01(v);
    }
  }

  const flagsArr = Array.isArray(raw.flags) ? raw.flags : [];
  const flags: string[] = flagsArr.filter((s): s is string => typeof s === "string");

  return {
    vendor_name: raw.vendor_name,
    vendor_address: asStringOrNull(raw.vendor_address),
    proposal_number: asStringOrNull(raw.proposal_number),
    proposal_date: asStringOrNull(raw.proposal_date),
    valid_through: asStringOrNull(raw.valid_through),
    title: raw.title,
    total_cents: totalCents,
    scope_summary: raw.scope_summary,
    inclusions: asStringOrNull(raw.inclusions),
    exclusions: asStringOrNull(raw.exclusions),
    notes: asStringOrNull(raw.notes),
    vendor_stated_start_date: asStringOrNull(raw.vendor_stated_start_date),
    vendor_stated_duration_days: asIntOrNull(raw.vendor_stated_duration_days),
    line_items,
    confidence_score: clamp01(raw.confidence_score),
    confidence_details: cleanedConfidenceDetails,
    flags,
    raw_response: raw,
  };
}

