// extractProposal — public entry point
// ─────────────────────────────────────────────────────────────────

export async function extractProposal(
  input: ExtractProposalInput,
  meta: ExtractProposalMeta
): Promise<ParsedProposal> {
  const base64 = toBuffer(input.pdfBuffer).toString("base64");

  const systemBlocks: Anthropic.Messages.TextBlockParam[] = [
    {
      type: "text",
      text: PROPOSAL_EXTRACT_SYSTEM_PROMPT,
      cache_control: { type: "ephemeral" },
    },
  ];

  const userContent: Anthropic.Messages.ContentBlockParam[] = [
    {
      type: "document",
      source: {
        type: "base64",
        media_type: "application/pdf",
        data: base64,
      },
    },
    {
      type: "text",
      text: "Extract the attached construction proposal. Return only the JSON object.",
    },
  ];

  const response = await callClaude({
    model: "claude-sonnet-4-20250514",
    max_tokens: 4096,
    system: systemBlocks,
    messages: [{ role: "user", content: userContent }],
    function_type: "proposal_extract",
    org_id: meta.org_id,
    user_id: meta.user_id ?? null,
    metadata: { ...meta.metadata, document_id: input.documentId },
  });

  const textBlock = response.content.find((block) => block.type === "text");
  if (!textBlock || textBlock.type !== "text") {
    throw new Error("Proposal extractor returned no text content block");
  }

  const cleaned = stripCodeFences(textBlock.text);
  let parsed: RawProposal;
  try {
    parsed = JSON.parse(cleaned) as RawProposal;
  } catch (err) {
    const msg = err instanceof Error ? err.message : "unknown";
    throw new Error(
      `Proposal extractor returned invalid JSON: ${msg}. First 200 chars: ${cleaned.slice(0, 200)}`
    );
  }

  return normalizeProposal(parsed);
}
